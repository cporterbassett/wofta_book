#!/usr/bin/env python3
"""
Reapply the draft ABC's $ linebreak positions to the candidate ABC.

The draft was generated by the OMR batch pipeline from the original scan,
so its $ markers reflect the scan's actual line layout (measures per line).
We extract cumulative barline counts at each $ in the draft's V:1, then
re-insert $ at the same positions in the candidate's V:1.

Usage: normalize_linebreaks.py <candidate.abc> <draft.abc> [output.abc]
If output.abc is omitted, overwrites candidate.abc in place.
"""
import re
import sys

# Matches any barline token: |: :| :: || | (in that order, longest first)
BAR_RE = re.compile(r'\|:|:\||::|(?<!\|)\|\|(?!\|)|\|')


def get_v1_body(abc_text):
    """Return (header, v1_body, tail) for the first V:1 note block."""
    # The ABC body has a V:1 declaration line then another V:1 (notes start).
    # We want the second V:1 block (the note content).
    m = list(re.finditer(r'^V:\s*1\s*(?:treble)?\s*$', abc_text, re.MULTILINE))
    if not m:
        return abc_text, '', ''
    start_match = m[-1]  # last V:1 declaration = note block
    body_start = start_match.end()
    # Find next voice declaration or end of file
    tail_m = re.search(r'^V:\s*\d', abc_text[body_start:], re.MULTILINE)
    if tail_m:
        body_end = body_start + tail_m.start()
        return abc_text[:body_start], abc_text[body_start:body_end], abc_text[body_end:]
    return abc_text[:body_start], abc_text[body_start:], ''


def count_bars(text):
    """Count barline tokens in a text segment, skipping lyrics/words lines."""
    lines = [l for l in text.splitlines() if not re.match(r'^[wW]:', l.strip())]
    t = '\n'.join(lines)
    t = re.sub(r'"[^"]*"', '', t)            # chord symbols
    t = re.sub(r'\{[^}]*\}', '', t)           # grace notes
    t = re.sub(r'\[[A-Za-z]:[^\]]*\]', '', t) # inline fields [K:...] etc.
    return len(BAR_RE.findall(t))


def get_break_bar_counts(body):
    """Return cumulative barline count at each $ in the body."""
    segments = body.split('$')
    counts, total = [], 0
    for seg in segments[:-1]:
        total += count_bars(seg)
        counts.append(total)
    return counts


def insert_breaks(body, target_counts):
    """Re-insert $ into body after each barline count in target_counts."""
    if not target_counts:
        return body
    targets = sorted(set(target_counts))
    result = []
    bar_count = 0
    break_idx = 0
    i = 0
    while i < len(body):
        # Try to match a barline token
        m = BAR_RE.match(body, i)
        if m:
            result.append(m.group())
            bar_count += 1
            i = m.end()
            while break_idx < len(targets) and bar_count >= targets[break_idx]:
                result.append('$')
                break_idx += 1
        else:
            result.append(body[i])
            i += 1
    return ''.join(result)


def normalize(candidate_abc, draft_abc):
    _, draft_v1, _ = get_v1_body(draft_abc)
    targets = get_break_bar_counts(draft_v1)
    if not targets:
        print("  (no $ breaks found in draft — skipping normalization)", file=sys.stderr)
        return candidate_abc

    pre, cand_v1, post = get_v1_body(candidate_abc)
    if not cand_v1:
        print("  (no V:1 body found in candidate — skipping normalization)", file=sys.stderr)
        return candidate_abc

    clean_v1 = cand_v1.replace('$', '')
    new_v1 = insert_breaks(clean_v1, targets)
    return pre + new_v1 + post


if __name__ == '__main__':
    if len(sys.argv) < 3:
        print(f"Usage: {sys.argv[0]} candidate.abc draft.abc [output.abc]")
        sys.exit(1)
    candidate = open(sys.argv[1]).read()
    draft = open(sys.argv[2]).read()
    output_path = sys.argv[3] if len(sys.argv) > 3 else sys.argv[1]
    result = normalize(candidate, draft)
    open(output_path, 'w').write(result)
    print(f"  Linebreaks normalized from draft → {output_path}")
